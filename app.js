async function renderMermaid() {
  const diagram = document.getElementById("mermaidDiagram");
  const source = document.getElementById("mermaidSourceView");
  const mermaidSource = getSelectedMermaidSource();
  diagram.textContent = mermaidSource;
  source.textContent = mermaidSource;
  diagram.classList.add("mermaid");
  diagram.removeAttribute("data-processed");
  document.getElementById("mermaidFallbackNotice").classList.add("hidden");

  if (!window.mermaid || typeof window.mermaid.render !== "function") {
    showMermaidFallback("Mermaid 렌더러를 찾지 못해 시퀀스 원문을 표시합니다. GitHub Pages에는 vendor/mermaid.min.js 파일이 index.html과 같은 배포 경로 기준으로 있어야 합니다.");
    return;
  }

  try {
    window.mermaid.initialize({ startOnLoad: false, securityLevel: "loose", theme: "base" });
    const renderId = `safe-trade-sequence-${Date.now()}`;
    const result = await window.mermaid.render(renderId, mermaidSource);
    diagram.classList.remove("mermaid");
    diagram.innerHTML = result.svg || result;
    if (typeof result.bindFunctions === "function") result.bindFunctions(diagram);
    settleCurrentHashScroll();
  } catch (error) {
    console.error("Mermaid render failed", error);
    showMermaidFallback("Mermaid 파일은 로드되었지만 시퀀스 렌더링 중 오류가 발생해 원문을 표시합니다. 브라우저 콘솔의 'Mermaid render failed' 메시지를 확인해 주세요.");
  }
}

function scrollToSection(id, behavior = "auto") {
  const target = document.getElementById(id);
  if (!target) return;
  target.scrollIntoView({ block: "start", behavior });
}

function settleCurrentHashScroll() {
  const id = window.location.hash.slice(1);
  if (!id) return;
  [0, 250, 800].forEach((delay) => {
    window.setTimeout(() => scrollToSection(id, "auto"), delay);
  });
}

function wireInteractions() {
  document.querySelector("nav").addEventListener("click", (event) => {
    const link = event.target.closest('a[href^="#"]');
    if (!link) return;
    const id = link.getAttribute("href").slice(1);
    if (!document.getElementById(id)) return;
    event.preventDefault();
    history.pushState(null, "", `#${id}`);
    scrollToSection(id);
  });

  window.addEventListener("hashchange", settleCurrentHashScroll);

  document.getElementById("sequenceVariant").addEventListener("click", (event) => {
    const button = event.target.closest("[data-sequence]");
    if (!button) return;
    selectedSequenceId = button.dataset.sequence;
    selectedStepId = getActiveSteps()[0].id;
    renderSequenceTabs();
    renderMermaid();
    renderSteps();
  });

  document.getElementById("stepRail").addEventListener("click", (event) => {
    const card = event.target.closest("[data-step]");
    if (!card) return;
    selectedStepId = card.dataset.step;
    renderSteps();
  });

  document.getElementById("roleFilter").addEventListener("click", (event) => {
    const button = event.target.closest("[data-role]");
    if (!button) return;
    selectedRole = button.dataset.role;
    renderSteps();
  });

  document.getElementById("policyDocs").addEventListener("click", (event) => {
    const button = event.target.closest("[data-policy-doc]");
    if (!button) return;
    selectedPolicyDocId = button.dataset.policyDoc;
    renderPolicyDocs();
  });

  document.getElementById("toggleMermaidSource").addEventListener("click", () => {
    document.getElementById("mermaidSourceView").classList.toggle("hidden");
  });

  const globalSearch = document.getElementById("globalSearch");
  if (globalSearch) {
    globalSearch.addEventListener("input", applySearch);
    globalSearch.addEventListener("search", applySearch);
    globalSearch.addEventListener("change", applySearch);
    globalSearch.addEventListener("keydown", (event) => {
      if (event.key !== "Enter") return;
      event.preventDefault();
      moveSearchResult(event.shiftKey ? -1 : 1);
    });
  }
  document.getElementById("searchPrev")?.addEventListener("click", () => moveSearchResult(-1));
  document.getElementById("searchNext")?.addEventListener("click", () => moveSearchResult(1));
  document.getElementById("downloadJson")?.addEventListener("click", () => download("usdt-safe-trade-sequence.json", JSON.stringify(makeExportData(), null, 2), "application/json"));
  document.getElementById("downloadMd")?.addEventListener("click", () => download("usdt-safe-trade-sequence-summary.md", makeMarkdown(), "text/markdown"));

  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries.filter((entry) => entry.isIntersecting).sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      document.querySelectorAll(".nav-link").forEach((link) => {
        link.classList.toggle("active", link.getAttribute("href") === `#${visible.target.id}`);
      });
    },
    { rootMargin: "-20% 0px -65% 0px", threshold: [0.01, 0.1, 0.3] }
  );
  appData.navigation.forEach(([id]) => {
    const section = document.getElementById(id);
    if (section) observer.observe(section);
  });
}

function applySearch() {
  const input = document.getElementById("globalSearch");
  const query = input.value.trim();
  const summary = document.getElementById("searchSummary");
  const sections = Array.from(document.querySelectorAll("main .section-panel"));
  const targets = Array.from(
    new Set(
      document.querySelectorAll(
        [
          "[data-searchable]",
          "tbody tr",
          ".article-clause",
          ".guide-step",
          ".qa-item",
          ".popup-topic",
          ".markdown-body h2",
          ".markdown-body h3",
          ".markdown-body h4",
          ".markdown-body p",
          ".markdown-body li",
          ".markdown-body td",
          ".markdown-body th",
          ".markdown-code"
        ].join(", ")
      )
    )
  );

  clearSearchHighlights();

  if (!query) {
    sections.forEach((section) => section.classList.remove("search-section-hit", "search-section-miss"));
    targets.forEach((node) => node.classList.remove("filtered-out", "match-hit"));
    document.querySelectorAll(".nav-link").forEach((link) => link.classList.remove("search-hit"));
    if (summary) summary.textContent = "";
    setSearchResults([]);
    return;
  }

  const terms = getSearchTerms(query);
  const matchedSectionIds = new Set();
  let matchedTargetCount = 0;

  sections.forEach((section) => {
    const matched = matchesSearch(section.textContent, terms);
    section.classList.toggle("search-section-hit", matched);
    section.classList.toggle("search-section-miss", !matched);
    if (matched) {
      matchedSectionIds.add(section.id);
      highlightMatches(section, terms);
    }
  });

  targets.forEach((node) => {
    const section = node.closest(".section-panel");
    const matched = section && matchedSectionIds.has(section.id) && matchesSearch(node.textContent, terms);
    node.classList.toggle("filtered-out", !matched);
    node.classList.toggle("match-hit", matched && node.matches("[data-searchable]"));
    if (matched) matchedTargetCount += 1;
  });

  document.querySelectorAll(".nav-link").forEach((link) => {
    const id = link.getAttribute("href")?.slice(1);
    link.classList.toggle("search-hit", matchedSectionIds.has(id));
  });

  if (summary) {
    summary.textContent = matchedSectionIds.size
      ? `${matchedSectionIds.size}개 섹션 · ${matchedTargetCount}개 항목 검색됨`
      : "일치하는 본문이 없습니다";
  }
  setSearchResults(getVisibleSearchHighlights(), { scroll: true });
}

function getVisibleSearchHighlights() {
  return Array.from(document.querySelectorAll("mark.search-highlight")).filter(
    (mark) => !mark.closest(".search-section-miss, .filtered-out, .hidden")
  );
}

function setSearchResults(marks, options = {}) {
  searchResultMarks.forEach((mark) => mark.classList.remove("active-search-highlight"));
  searchResultMarks = marks;
  activeSearchResultIndex = marks.length ? 0 : -1;
  updateSearchNavigation();
  if (options.scroll && marks.length) scrollToSearchResult(activeSearchResultIndex);
}

function moveSearchResult(direction) {
  if (!searchResultMarks.length) return;
  activeSearchResultIndex = (activeSearchResultIndex + direction + searchResultMarks.length) % searchResultMarks.length;
  updateSearchNavigation();
  scrollToSearchResult(activeSearchResultIndex);
}

function updateSearchNavigation() {
  searchResultMarks.forEach((mark, index) => {
    mark.classList.toggle("active-search-highlight", index === activeSearchResultIndex);
  });

  const count = document.getElementById("searchCount");
  const prev = document.getElementById("searchPrev");
  const next = document.getElementById("searchNext");
  const hasResults = searchResultMarks.length > 0;

  if (count) {
    count.textContent = hasResults ? `${activeSearchResultIndex + 1} / ${searchResultMarks.length}` : "0 / 0";
  }
  [prev, next].forEach((button) => {
    if (button) button.disabled = !hasResults;
  });
}

function scrollToSearchResult(index) {
  const mark = searchResultMarks[index];
  if (!mark) return;
  mark.scrollIntoView({ behavior: "smooth", block: "center", inline: "nearest" });
}

function getSearchTerms(query) {
  return query
    .normalize("NFKC")
    .split(/\s+/)
    .map((term) => term.trim())
    .filter(Boolean)
    .map((term) => ({
      raw: term,
      lower: term.toLowerCase(),
      compact: compactSearchText(term)
    }))
    .filter((term, index, terms) => term.compact && terms.findIndex((item) => item.compact === term.compact) === index);
}

function compactSearchText(value) {
  return String(value)
    .normalize("NFKC")
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, "");
}

function matchesSearch(text, terms) {
  const lower = String(text).normalize("NFKC").toLowerCase();
  const compact = compactSearchText(text);
  return terms.every((term) => lower.includes(term.lower) || compact.includes(term.compact));
}

function clearSearchHighlights() {
  document.querySelectorAll("mark.search-highlight").forEach((mark) => {
    const parent = mark.parentNode;
    mark.replaceWith(document.createTextNode(mark.textContent));
    if (parent) parent.normalize();
  });
}

function highlightMatches(root, terms) {
  const words = terms
    .flatMap((term) => term.raw.split(/\s+/))
    .map((term) => term.trim())
    .filter(Boolean)
    .sort((a, b) => b.length - a.length);
  if (!words.length) return;

  const pattern = `(${words.map(escapeRegExp).join("|")})`;
  const matcher = new RegExp(pattern, "i");
  const splitter = new RegExp(pattern, "gi");
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      if (parent.closest("mark, script, style, svg, input, textarea")) return NodeFilter.FILTER_REJECT;
      return matcher.test(node.nodeValue) ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach((node) => {
    const fragment = document.createDocumentFragment();
    const parts = node.nodeValue.split(splitter);
    parts.forEach((part) => {
      if (!part) return;
      if (matcher.test(part)) {
        const mark = document.createElement("mark");
        mark.className = "search-highlight";
        mark.textContent = part;
        fragment.appendChild(mark);
      } else {
        fragment.appendChild(document.createTextNode(part));
      }
    });
    node.replaceWith(fragment);
  });
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function download(filename, content, type) {
  const blob = new Blob([content], { type: `${type};charset=utf-8` });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

