function showMermaidFallback(message) {
  const source = document.getElementById("mermaidSourceView");
  const notice = document.getElementById("mermaidFallbackNotice");
  notice.textContent = message;
  notice.classList.remove("hidden");
  source.classList.remove("hidden");
}

async function renderMermaid() {
  const diagram = document.getElementById("mermaidDiagram");
  const source = document.getElementById("mermaidSourceView");
  const mermaidSource = getSelectedMermaidSource();
  diagram.textContent = mermaidSource;
  source.textContent = mermaidSource;
  diagram.classList.add("mermaid");
  diagram.removeAttribute("data-processed");
  document.getElementById("mermaidFallbackNotice").classList.add("hidden");

  if (!window.mermaid || typeof window.mermaid.render !== "function") {
    showMermaidFallback("Mermaid 렌더러를 찾지 못해 시퀀스 원문을 표시합니다. GitHub Pages에는 vendor/mermaid.min.js 파일이 index.html과 같은 배포 경로 기준으로 있어야 합니다.");
    return;
  }

  try {
    window.mermaid.initialize({ startOnLoad: false, securityLevel: "loose", theme: "base" });
    const renderId = `safe-trade-sequence-${Date.now()}`;
    const result = await window.mermaid.render(renderId, mermaidSource);
    diagram.classList.remove("mermaid");
    diagram.innerHTML = result.svg || result;
    if (typeof result.bindFunctions === "function") result.bindFunctions(diagram);
    settleCurrentHashScroll();
  } catch (error) {
    console.error("Mermaid render failed", error);
    showMermaidFallback("Mermaid 파일은 로드되었지만 시퀀스 렌더링 중 오류가 발생해 원문을 표시합니다. 브라우저 콘솔의 'Mermaid render failed' 메시지를 확인해 주세요.");
  }
}

function scrollToSection(id, behavior = "smooth") {
  const target = document.getElementById(id);
  if (!target) return;
  target.scrollIntoView({ block: "start", behavior });
}

function settleCurrentHashScroll() {
  const id = window.location.hash.slice(1);
  if (!id) return;
  [0, 250, 800].forEach((delay) => {
    window.setTimeout(() => scrollToSection(id, "auto"), delay);
  });
}

function wireInteractions() {
  document.querySelector("nav").addEventListener("click", (event) => {
    const link = event.target.closest('a[href^="#"]');
    if (!link) return;
    const id = link.getAttribute("href").slice(1);
    if (!document.getElementById(id)) return;
    event.preventDefault();
    history.pushState(null, "", `#${id}`);
    scrollToSection(id);
  });

  window.addEventListener("hashchange", settleCurrentHashScroll);

  document.getElementById("sequenceVariant").addEventListener("click", (event) => {
    const button = event.target.closest("[data-sequence]");
    if (!button) return;
    selectedSequenceId = button.dataset.sequence;
    selectedStepId = getActiveSteps()[0].id;
    renderSequenceTabs();
    renderMermaid();
    renderSteps();
  });

  document.getElementById("stepRail").addEventListener("click", (event) => {
    const card = event.target.closest("[data-step]");
    if (!card) return;
    selectedStepId = card.dataset.step;
    renderSteps();
  });

  document.getElementById("roleFilter").addEventListener("click", (event) => {
    const button = event.target.closest("[data-role]");
    if (!button) return;
    selectedRole = button.dataset.role;
    renderSteps();
  });

  document.getElementById("policyDocs").addEventListener("click", (event) => {
    const button = event.target.closest("[data-policy-doc]");
    if (!button) return;
    selectedPolicyDocId = button.dataset.policyDoc;
    renderPolicyDocs();
  });

  document.getElementById("toggleMermaidSource").addEventListener("click", () => {
    document.getElementById("mermaidSourceView").classList.toggle("hidden");
  });

  document.getElementById("globalSearch").addEventListener("input", applySearch);
  document.getElementById("downloadJson").addEventListener("click", () => download("usdt-safe-trade-sequence.json", JSON.stringify(appData, null, 2), "application/json"));
  document.getElementById("downloadMd").addEventListener("click", () => download("usdt-safe-trade-sequence-summary.md", makeMarkdown(), "text/markdown"));

  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries.filter((entry) => entry.isIntersecting).sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      document.querySelectorAll(".nav-link").forEach((link) => {
        link.classList.toggle("active", link.getAttribute("href") === `#${visible.target.id}`);
      });
    },
    { rootMargin: "-20% 0px -65% 0px", threshold: [0.01, 0.1, 0.3] }
  );
  appData.navigation.forEach(([id]) => {
    const section = document.getElementById(id);
    if (section) observer.observe(section);
  });
}

function applySearch() {
  const query = document.getElementById("globalSearch").value.trim().toLowerCase();
  document.querySelectorAll("[data-searchable], tbody tr").forEach((node) => {
    const matched = !query || node.textContent.toLowerCase().includes(query);
    node.classList.toggle("filtered-out", !matched);
    node.classList.toggle("match-hit", Boolean(query && matched && node.matches("[data-searchable]")));
  });
}

function download(filename, content, type) {
  const blob = new Blob([content], { type: `${type};charset=utf-8` });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

