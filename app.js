  const kpis = [
    ["7", "고객 경험 단계"],
    ["20", "내부 상태값"],
    ["18", "핵심 버튼 정책"],
    ["11", "모니터링 항목"]
  ];
  target.innerHTML = kpis
    .map(([value, label]) => `<div class="kpi-card"><strong>${value}</strong><span>${label}</span></div>`)
    .join("");
