function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderTable(headers, rows, options = {}) {
  const body = rows
    .map((row) => `<tr>${row.map((cell, index) => `<td>${formatCell(cell, index, options)}</td>`).join("")}</tr>`)
    .join("");
  return `
    <table>
      <thead><tr>${headers.map((header) => `<th>${escapeHtml(header)}</th>`).join("")}</tr></thead>
      <tbody>${body}</tbody>
    </table>
  `;
}

function getPolicyDocGroups() {
  const docsById = new Map(appData.policyDocs.map((doc) => [doc.id, doc]));
  const groupedIds = new Set(policyDocGroups.flatMap((group) => group.ids));
  const groups = policyDocGroups
    .map((group) => ({
      ...group,
      docs: group.ids.map((id) => docsById.get(id)).filter(Boolean)
    }))
    .filter((group) => group.docs.length);
  const ungroupedDocs = appData.policyDocs.filter((doc) => !groupedIds.has(doc.id));

  if (!ungroupedDocs.length) return groups;
  return [...groups, { title: "기타", docs: ungroupedDocs }];
}

function formatCell(cell, index, options) {
  if (options.codeFirst && index === 0) return `<code>${escapeHtml(cell)}</code>`;
  if (options.statusFirst && index === 0) return `<span class="status-chip">${escapeHtml(cell)}</span>`;
  return escapeHtml(cell);
}

